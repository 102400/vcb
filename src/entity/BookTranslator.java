package entity;

public class BookTranslator {
	
	private int id;
	private int bookId;
	private int bookTranslatorId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public int getBookTranslatorId() {
		return bookTranslatorId;
	}
	public void setBookTranslatorId(int bookTranslatorId) {
		this.bookTranslatorId = bookTranslatorId;
	}

}
