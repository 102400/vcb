package entity;

public class BookInfo {
	
	private int bookInfoId;
	private String name;
	
	public int getBookInfoId() {
		return bookInfoId;
	}
	public void setBookInfoId(int bookInfoId) {
		this.bookInfoId = bookInfoId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}